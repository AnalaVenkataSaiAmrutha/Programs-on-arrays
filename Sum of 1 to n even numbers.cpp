/*Sum 1 to n even numbers using arrays*/
#include<stdio.h>
int main()
{
	int a[50],i,n,sum=0;
	printf("Enter the size of array:\n");
	scanf("%d",&n);
	printf("Enter array elements:\n");
	for(i=0;i<n;i++)
	{
		scanf("%d",&a[i]);
	}
	for(i=0;i<n;i++)
	{
		if(a[i]%2==0)
          sum=sum+a[i];
		  		
	}
	printf("Sum 1 to %d even numbers is %d\n",n,sum);
	return 0;
}
